import random
# 1. Generate a random integer within a specific range (e.g., between 10 and 50)
random_int = random.randint(10, 50)
print("Random integer between 10 and 50:", random_int)

# 2. Generate a random float between 1 and 10
random_float = random.uniform(1, 10)
print("Random float between 1 and 10:",random_float)